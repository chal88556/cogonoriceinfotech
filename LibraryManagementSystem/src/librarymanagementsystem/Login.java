    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
     
        String ID = t1.getText();
        String pass = t2.getText();

            try {
                Class.forName("com.mysql.cj.jdbc.Driver");
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost/library?useSSL=false", "root", "");
                String sql = "SELECT * FROM user WHERE User_ID = ? AND Password = ?";
                PreparedStatement st = con.prepareStatement(sql);
                st.setString(1, ID);
                st.setString(2, pass);

                ResultSet rs = st.executeQuery();

                boolean userFound = false;

                while (rs.next()) {
                    String username = rs.getString("User_ID");
                    String password = rs.getString("Password");
                    String admin = rs.getString("Admin");

                    // Check if the user is an admin
                    if (ID.equals(username) && pass.equals(password) && !"0".equals(admin)) {
                        userFound = true;
                        String name = rs.getString("Username");
                        new Functions(name).setVisible(true);
                        setVisible(false);
                        break;
                    }
                }

                if (!userFound) {
                    JOptionPane.showMessageDialog(null, "Username or password is incorrect");
                }

                con.close();
            } catch (ClassNotFoundException e) {
                JOptionPane.showMessageDialog(null, "Error loading JDBC driver: " + e.getMessage());
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "SQL Error: " + e.getMessage());
            }

    }//GEN-LAST:event_jButton1ActionPerformed

   
