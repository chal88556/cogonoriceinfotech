    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        
 String user = t1.getText();
String pass = t2.getText();

try {
    Class.forName("com.mysql.cj.jdbc.Driver");
    Connection con = DriverManager.getConnection("jdbc:mysql://localhost/college?useSSL=false", "root", "");

    Statement st = con.createStatement();
    String sql = "SELECT * FROM Admin";
    ResultSet rs = st.executeQuery(sql);

    boolean userFound = false;

    while (rs.next()) {
        String username = rs.getString("ID");
        String password = rs.getString("Password");
         String Name = rs.getString("Name");
        String Post = rs.getString("Post");


        if (user.equals(username) && pass.equals(password)) {
            userFound = true;
            // Open the Shop frame or perform other actions
              new AdminPortal(username,Name,Post).setVisible(true);
            setVisible(false);
            break;
        }
    }

    if (!userFound) {
        JOptionPane.showMessageDialog(null, "Username or password is incorrect");
    }
  con.close();
} catch (ClassNotFoundException e) {
    JOptionPane.showMessageDialog(null, "Error loading JDBC driver: " + e.getMessage());
} catch (SQLException e) {
    JOptionPane.showMessageDialog(null, "SQL Error: " + e.getMessage());
}


      

    }//GEN-LAST:event_jButton2ActionPerformed

