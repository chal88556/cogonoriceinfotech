    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        String ID=t1.getText();
        String pass=t2.getText();
       try {
    Class.forName("com.mysql.cj.jdbc.Driver");
    Connection con = DriverManager.getConnection("jdbc:mysql://localhost/college?useSSL=false", "root", "");

    Statement st = con.createStatement();
    String sql = "SELECT * FROM StudentInfo";
    ResultSet rs = st.executeQuery(sql);

    boolean userFound = false;

    while (rs.next()) {
        String username = rs.getString("ID");
        String password = rs.getString("Password");

        if (ID.equals(username) && pass.equals(password)) {
            userFound = true;
                String name  = rs.getString("FName");
                String lname=rs.getString("LName");
                int id = rs.getInt("ID");
                String email = rs.getString("Email");
                String course = rs.getString("Course");

                // Open the StudentInfo JFrame and populate its fields with the fetched data
            new StudentInfo(name,lname,id,email,course).setVisible(true);
            setVisible(false);

            break;
        }
    }

    if (!userFound) {
        JOptionPane.showMessageDialog(null, "Username or password is incorrect");
    }
  con.close();
} catch (ClassNotFoundException e) {
    JOptionPane.showMessageDialog(null, "Error loading JDBC driver: " + e.getMessage());
} catch (SQLException e) {
    JOptionPane.showMessageDialog(null, "SQL Error: " + e.getMessage());
}     
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

